package com.example.stoper;

public class timeData
{
   long data;
   int num;

   public timeData(long data, int num)
   {
      this.data = data;
      this.num = num;
   }

   public long getData()
   {
      return data;
   }

   public void setData(long data)
   {
      this.data = data;
   }

   public int getNum()
   {
      return num;
   }

   public void setNum(int num)
   {
      this.num = num;
   }
}
